  package HashMap;
  import java.util.*;
  
  public class Hash {
     public static void main(String[] args) {
       
       HashMap<String, Integer> map = new HashMap<>();
       map.put("v",10);
       map.put("z",120);
       map.put("t",130);
       map.put("r",140);
       System.out.println(map);
       int a = map.get("t");
       System.out.println(a);
       if (map.containsKey("r")) {
         System.out.println(map.get("r"));
       }
       map.clear();
       if (map.isEmpty()) {
        System.out.println("Empty");
       }}
     }